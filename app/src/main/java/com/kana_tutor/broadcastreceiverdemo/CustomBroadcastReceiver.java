package com.kana_tutor.broadcastreceiverdemo;

import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;
import android.util.Log;
import android.widget.Toast;

public class CustomBroadcastReceiver extends BroadcastReceiver {
    private static final String TAG = CustomBroadcastReceiver.class.getSimpleName();
    @Override
    public void onReceive(Context context, Intent intent) {
        Toast.makeText(context, "Intent received", Toast.LENGTH_LONG)
             .show();
        Log.d(TAG, "Intent received:" + intent.toString());
    }
}
